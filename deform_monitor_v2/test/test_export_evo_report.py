"""Unit tests for export_evo_report.py.

The fixed timestamps and metric values in this file are synthetic parser and
formatter fixtures. They exist only to verify EVO text parsing and report
layout, and they do not represent benchmark or paper results.
"""

import importlib.util
import pathlib
import shlex
import tempfile
import unittest
from types import SimpleNamespace


SCRIPT_PATH = pathlib.Path(__file__).resolve().parents[1] / "scripts" / "export_evo_report.py"


def load_module_if_exists():
    if not SCRIPT_PATH.exists():
        return None
    spec = importlib.util.spec_from_file_location("export_evo_report", SCRIPT_PATH)
    module = importlib.util.module_from_spec(spec)
    spec.loader.exec_module(module)
    return module


