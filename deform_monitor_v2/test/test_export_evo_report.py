    def test_run_evo_command_sets_headless_backend_and_returns_stdout(self):
        module = load_module_if_exists()
        if module is None:
            self.fail(f"Missing implementation script: {SCRIPT_PATH}")

        captured = {}

        def fake_run(cmd, check, capture_output, text, env):
            captured["cmd"] = cmd
            captured["check"] = check
            captured["capture_output"] = capture_output
            captured["text"] = text
            captured["env"] = env
            return SimpleNamespace(stdout="fake stdout\n", stderr="")

        original_run = module.subprocess.run
        module.subprocess.run = fake_run
        try:
            stdout_text = module.run_evo_command(
                ["evo_ape", "tum", "/tmp/gt.tum", "/tmp/odom.tum", "--align_origin"]
            )
        finally:
            module.subprocess.run = original_run

        self.assertEqual(stdout_text, "fake stdout\n")
        self.assertEqual(captured["cmd"][0], "evo_ape")
        self.assertTrue(captured["check"])
        self.assertTrue(captured["capture_output"])
        self.assertTrue(captured["text"])
        self.assertEqual(captured["env"]["MPLBACKEND"], "Agg")

